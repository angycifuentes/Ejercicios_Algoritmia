# Nombre: Programa para identificar si un numero es positivo o no

# Entrada:
# Numero digitado 

# Salida: 
# Identificar si el numero es positivo o no 

# Proceso: Pedir un numero y extraer si es positivo o no

# Pedir un numero 
numero = int(input("Ingrese un número: "))

#Mostrar el resultado 
if numero > 0:
        print("El número es positivo")
else:
        print("El número no es positivo")
